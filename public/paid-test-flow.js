(function(){
  function qsa(selector){return Array.prototype.slice.call(document.querySelectorAll(selector));}
  function textOf(el){return (el.textContent||'').toLowerCase().replace(/\s+/g,' ').trim();}
  function isPaidButton(el){
    var text=textOf(el);
    var href=(el.getAttribute('href')||'').toLowerCase();
    return text.includes('paid report') || text.includes('advanced report') || text.includes('go deeper') || text.includes('detailed report') || href.includes('paid') || href.includes('advanced');
  }
  function getVal(id){var el=document.getElementById(id);return el?String(el.value||'').trim():'';}
  function findInputByName(names){
    for(var i=0;i<names.length;i++){
      var el=document.querySelector('[name="'+names[i]+'"], #'+names[i]);
      if(el) return String(el.value||'').trim();
    }
    return '';
  }
  function collectPayload(){
    return {
      name:getVal('popName')||getVal('name')||findInputByName(['name','fullName','full_name']),
      email:getVal('popEmail')||getVal('email')||findInputByName(['email']),
      phone:getVal('popPhone')||getVal('phone')||getVal('whatsapp')||findInputByName(['phone','whatsapp','whatsapp_number']),
      dob:getVal('popDob')||getVal('dob')||findInputByName(['dob','date_of_birth']),
      question:getVal('popQuestion')||getVal('question')||'Paid detailed numerology report',
      source:'paid_report_test_button'
    };
  }
  function ensurePaidBox(){
    var box=document.getElementById('paidTestReportBox');
    if(box) return box;
    box=document.createElement('div');
    box.id='paidTestReportBox';
    box.style.cssText='position:fixed;inset:auto 18px 18px auto;z-index:99999;width:min(420px,calc(100vw - 36px));max-height:72vh;overflow:auto;background:#111015;color:#f4efe7;border:1px solid rgba(201,169,110,.35);box-shadow:0 24px 90px rgba(0,0,0,.45);border-radius:14px;padding:18px;font-family:Arial,sans-serif;display:none;';
    document.body.appendChild(box);
    return box;
  }
  function setBox(html){var box=ensurePaidBox();box.innerHTML=html;box.style.display='block';}
  function closeBox(){var box=document.getElementById('paidTestReportBox');if(box)box.style.display='none';}
  async function generatePaidReport(){
    var payload=collectPayload();
    if(!payload.name || !payload.email || !payload.phone || !payload.dob){
      if(typeof openFreeReportPopup==='function') openFreeReportPopup();
      setBox('<button style="float:right;background:none;border:0;color:#c9a96e;font-size:22px" onclick="document.getElementById(\'paidTestReportBox\').style.display=\'none\'">&times;</button><h3 style="margin:0 0 8px;color:#c9a96e">Enter details first</h3><p style="line-height:1.5;color:#cfc6bb">Please fill name, email, WhatsApp number and date of birth in the report form. Then click Paid Report again.</p>');
      return;
    }
    setBox('<button style="float:right;background:none;border:0;color:#c9a96e;font-size:22px" onclick="document.getElementById(\'paidTestReportBox\').style.display=\'none\'">&times;</button><h3 style="margin:0 0 8px;color:#c9a96e">Generating paid test report</h3><p style="line-height:1.5;color:#cfc6bb">This is using the paid report model. It may take 1 to 2 minutes.</p><div style="height:6px;background:#24202b;border-radius:99px;overflow:hidden"><div style="height:100%;width:45%;background:#c9a96e;animation:paidPulse 1.2s infinite alternate"></div></div><style>@keyframes paidPulse{from{width:25%}to{width:95%}}</style>');
    try{
      var res=await fetch('/api/reports/paid-test',{method:'POST',headers:{'Content-Type':'application/json'},body:JSON.stringify(payload)});
      var data=await res.json();
      if(!res.ok || !data.success) throw new Error(data.error||'Paid report generation failed');
      var report=(data.report_text||'').replace(/\n/g,'<br>');
      setBox('<button style="float:right;background:none;border:0;color:#c9a96e;font-size:22px" onclick="document.getElementById(\'paidTestReportBox\').style.display=\'none\'">&times;</button><h3 style="margin:0 0 8px;color:#c9a96e">Paid Test Report Ready</h3><p style="color:#cfc6bb;margin:0 0 12px">Generated by: '+(data.generated_by||'paid model')+'</p><a href="'+data.pdf_url+'" target="_blank" style="display:block;text-align:center;background:#c9a96e;color:#08070a;text-decoration:none;font-weight:700;padding:12px 14px;border-radius:8px;margin-bottom:14px">Download PDF</a><div style="font-size:14px;line-height:1.65;color:#f4efe7">'+report+'</div>');
    }catch(err){
      setBox('<button style="float:right;background:none;border:0;color:#c9a96e;font-size:22px" onclick="document.getElementById(\'paidTestReportBox\').style.display=\'none\'">&times;</button><h3 style="margin:0 0 8px;color:#c9a96e">Paid test failed</h3><p style="line-height:1.5;color:#cfc6bb">'+String(err.message||err)+'</p>');
    }
  }
  function bind(){
    qsa('a,button').forEach(function(el){
      if(el.dataset.paidTestBound==='1') return;
      if(!isPaidButton(el)) return;
      el.dataset.paidTestBound='1';
      el.addEventListener('click',function(e){e.preventDefault();e.stopPropagation();generatePaidReport();},true);
    });
  }
  window.generatePaidTestReport=generatePaidReport;
  if(document.readyState==='loading')document.addEventListener('DOMContentLoaded',bind);else bind();
  setTimeout(bind,1200);setTimeout(bind,3000);
})();
